#cake 1=Black Forest
#Inventory cost of cake 1

print("This calculation of Inventory cost,profit,and profit percentage for a Black Forest cake:")
material_cost=200
transportation_cost=40
material_cost_and_transportation_cost=material_cost+transportation_cost
utility_cost=(3*material_cost_and_transportation_cost)/100
space_cost=50
staff_cost=60
print("material cost:",material_cost)
print("transportation cost:",transportation_cost)
print("utility cost:",utility_cost)
print("space cost:",space_cost)
print("staff cost:",staff_cost)

Inventory_cost=material_cost+transportation_cost+utility_cost+space_cost+staff_cost
The_selling_price_of_one_pound_Black_forest_cake=900
Profit_1=The_selling_price_of_one_pound_Black_forest_cake-Inventory_cost
The_percentage_of_profit=(Profit_1/Inventory_cost)*100
print("The inventory cost=",Inventory_cost)
print("The profit=",Profit_1)
print("The percentage of profit=",The_percentage_of_profit)
